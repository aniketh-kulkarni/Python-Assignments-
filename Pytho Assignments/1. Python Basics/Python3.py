#Define variables for different Data Types int, Boolean, char, float, double and print on the Console
# Define an int variable
myInt = 42
print("The value of myInt is:", myInt)

# Define a boolean variable
myBool = True
print("The value of myBool is:", myBool)

# Define a char variable (in Python, we use strings for characters)
myChar = 'A'
print("The value of myChar is:", myChar)

# Define a float variable
myFloat = 3.14
print("The value of myFloat is:", myFloat)

# Define a double variable (in Python, we use the float data type for double-precision numbers)
myDouble = 3.14159265359
print("The value of myDouble is:", myDouble)
